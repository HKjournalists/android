package com.kplus.car.model;

public class HistoryFrameAndMotor {
	private String vehicleNum;
	private String historyFrame;
	private String historyMotor;
	public String getVehicleNum() {
		return vehicleNum;
	}
	public void setVehicleNum(String vehicleNum) {
		this.vehicleNum = vehicleNum;
	}
	public String getHistoryFrame() {
		return historyFrame;
	}
	public void setHistoryFrame(String historyFrame) {
		this.historyFrame = historyFrame;
	}
	public String getHistoryMotor() {
		return historyMotor;
	}
	public void setHistoryMotor(String historyMotor) {
		this.historyMotor = historyMotor;
	}
	
}
